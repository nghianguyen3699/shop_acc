                            function buyProduct() {
                                var id = $("#modal-id").val();
                                var amount = $("#amount").val();
                                var token = $("#token").val();
                                $('#btnBuy').html('<i class="fa fa-spinner fa-spin"></i> Loading...').prop('disabled', true);
                                $.ajax({
                                    url: "localhost/ajaxs/client/buyProduct.php",
                                    method: "POST",
                                    dataType: "JSON",
                                    data: {
                                        token: token,
                                        id: id,
                                        coupon: $("#coupon").val(),
                                        amount: amount
                                    },
                                    success: function (data) {
                                        $('#btnBuy').html('<i class="fas fa-credit-card mr-1"></i>Thanh toán').prop(
                                            'disabled', false);
                                        if (data.status == 'success') {
                                            cuteToast({
                                                type: "success",
                                                message: data.msg,
                                                timer: 5000
                                            });
                                            $urlReturn = 'localhost/client/order/' + data.transid;
                                            setTimeout("location.href = '" + $urlReturn + "';", 1000);
                                        } else {
                                            Swal.fire(
                                                'Thất bại',
                                                data.msg,
                                                'error'
                                            );
                                        }
                                    },
                                    error: function () {
                                        $('#btnBuy').html('<i class="fas fa-credit-card mr-1"></i>Thanh toán').prop(
                                            'disabled', false);
                                        cuteToast({
                                            type: "error",
                                            message: 'Không thể xử lý',
                                            timer: 5000
                                        });
                                    }
                                });
                            }
